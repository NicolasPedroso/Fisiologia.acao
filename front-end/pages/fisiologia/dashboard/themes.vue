async function createItem() {
	if (!validForm.value) {
		notify({
			title: "Formulário inválido!",
			type: "warning",
			text: "Por favor, preencha todos os campos obrigatórios.",
		})
		return
	}

	loadingRes.value = true

	// Como não há arquivos, podemos enviar um JSON simples
	const payload = {
		title: formData.title,
		icon: formData.icon,
	}

	await useDataLoader("/api/themes", {
		method: "POST",
		body: payload,
	})
		.then(() => {
			refresh() // Recarrega a lista
			closeDialog()
			notify({
				title: "Sucesso!",
				text: "Tema criado com sucesso.",
				type: "success",
			})
		})
		.catch((error) => {
			console.error(error)
			notify({
				title: "Erro!",
				text: "Não foi possível criar o tema: " + formatError(error),
				type: "error",
			})
		})
		.finally(() => {
			loadingRes.value = false
		})
}

async function deleteItem() {
	loadingRes.value = true
	await useDataLoader(`/api/themes/${formData.id}`, {
		method: "DELETE",
	})
		.then(() => {
			refresh()
			closeDialog()
			notify({
				title: "Sucesso!",
				text: "Tema deletado com sucesso.",
				type: "success",
			})
		})
		.catch((error) => {
			console.error(error)
			notify({
				title: "Erro!",
				text: "Não foi possível deletar o tema.",
				type: "error",
			})
		})
		.finally(() => {
			loadingRes.value = false
		})
}
